      <p>
        I'm sure many of you have been eagerly awaiting this lesson. Yes. The infamous F-words, as I've called them. <img src="img/grin.gif"/> They are so confusing because of their dictionary entries. But in practice, I think that they are not that bad.</p>
      <p>
        <span class="navi">Mìftxele</span>, I recommend that you go back and read <a href="04.php">Lesson Four</a> to refresh your memory a bit about the little word, <strong>
          <span class="ygold;">a</span>
        </strong>. <img src="img/wink.gif"/>
      </p>
      <h2 id="fwa">Fwa</h2>
      <p>
        <span class="orange bold">Fw</span><span class="ygold bold">a</span> is short for <span class="orange bold">fì'u</span> <span class="ygold bold">a</span>. The word <span class="orange bold">fì'u</span> means <em>this thing</em>, or <em>this</em>. And I talked about <span class="ygold bold">a</span> in Lesson Four. It's just a little thingy you put between a noun and whatever phrase that describes it, while talking about what the noun is involved in. Since <span class="orange bold">fw</span><span class="ygold bold">a</span> is short for <span class="orange bold">fì'u</span> <span class="ygold bold">a</span>, this could be used any time you could use the word <span class="orange bold">fì'u</span>.
      </p>
      <p>
        <u>For example:</u>
      </p>
      <table>
        <tr>
          <td>
            Oeru <span class="vin">sunu</span> <span class="orange bold">fì'u</span>.
          </td>
          <td>
            (<em>
              <span class="orange bold">This</span> <span class="vin">is-pleasing</span> to me
            </em> -or- <em>I like this.</em>)
          </td>
        </tr>
        <tr>
          <td>
            Oeru <span class="vin">sunu</span> <span class="orange bold">fw</span><span class="ygold bold">a</span> <span class="ac">nga zera'u</span>.
          </td>
          <td>
            (<em>
              <span class="vin">is-pleasing</span> to me <span class="orange bold">this</span> <span class="ygold bold">which is</span> <span class="ac">you are coming</span>.
            </em> -or- <em>I like that you're coming.</em>)
          </td>
        </tr>
        <tr>
          <td>
            <span class="orange bold">Fì'u</span> <span class="adj">prrte'</span> <span class="vin">lu</span>.
          </td>
          <td>
            (<em>
              <span class="orange bold">This</span> <span class="vin">is</span> <span class="adj">pleasurable</span>.
            </em>)
          </td>
        </tr>
        <tr>
          <td>
            <span class="orange bold">Fw</span><span class="ygold bold">a</span> <span class="ac">tsun oeng pivängkxo</span> <span class="adj">prrte'</span> <span class="vin">lu</span>.
          </td>
          <td>
            (<em>
              <span class="orange bold">This</span> <span class="ygold bold">which is</span> <span class="ac">we can chat</span> <span class="vin">is</span> <span class="adj">pleasurable</span>.
            </em> -or- <em>That we can chat is pleasurable</em>)
          </td>
        </tr>
        <tr>
          <td>
            <span class="adj">Txantsan</span> <span class="vin">lu</span> <span class="orange bold">fì'u</span>.
          </td>
          <td>
            (<em>
              <span class="orange bold">This</span> <span class="vin">is</span> <span class="adj">excellent</span>.
            </em>)
          </td>
        </tr>
        <tr>
          <td>
            <span class="adj">Txantsan</span> <span class="vin">lu</span> <span class="orange bold">fw</span><span class="ygold bold">a</span> <span class="ac">tse'a ngati fìtseng</span>.
          </td>
          <td>
            (<em>
              <span class="adj">Excellent</span> <span class="vin">is</span> <span class="orange bold">this</span> <span class="ygold bold">which is</span> <span class="ac">see you here</span>.
            </em> -or- <em>It's excellent to see you here.</em>)
          </td>
        </tr>
      </table>
      <p>
        And to answer the question from the previous Lesson, like was said in the comments, in order to say <em>Hunting hexapede is fun</em> You say this:
      </p>
      <p>
        <span class="orange bold">Fw</span><span class="ygold bold">a</span> <span class="ac">taron yerikit</span> <span class="vin">lu</span> <span class="adj">'o'</span>. (<em>
          <span class="orange bold">This</span>
          <span class="ac">"hunt hexapede"</span>
          <span class="ygold bold">-</span>
          <span class="orange bold">thing</span>
          <span class="vin">is</span>
          <span class="adj">fun</span>
        </em>)
      </p>
      <p>
        You can also think of that as <em>It's fun to hunt hexapede.</em>
      </p>
      <p>
        Notice that <span class="orange bold">fì'u</span> and <span class="orange bold">fw</span><span class="ygold bold">a</span> are used when the main sentence is made with a <span class="vin">vin.</span> verb. i.e., a verb that you can't directly do to something. Such as sleep or fall. A lot of the time, <span class="orange bold">fw</span><span class="ygold bold">a</span> is seen with the verbs: <span class="vin">lam</span>, <span class="vin">sunu</span>, and <span class="vin">lu</span>.
      </p>
      <h2 id="fula">Fula</h2>
      <p>
        <span class="ag">Ful</span><span class="ygold bold">a</span> is short for <span class="orange bold">fì'u</span>-<span class="ag">l</span> <span class="ygold bold">a</span>. So this one could be used any time you could use <span class="ag">fì'ul</span>.
      </p>
      <p>
        <u>For example:</u>
      </p>
      <table>
        <tr>
          <td>
            <span class="vtr">Vìmingkap</span> <span class="red bold">oeti</span> <span class="ag">fì'ul</span>.
          </td>
          <td>
            (<em>
              <span class="vtr">Just occured to</span> <span class="red bold">me</span> <span class="ag">this</span>.
            </em> -or- <em>This just occured to me.</em>)
          </td>
        </tr>
        <tr>
          <td>
            <span class="vtr">Vìmingkap</span> <span class="red bold">oeti</span> <span class="ag">ful</span><span class="ygold bold">a</span> <span class="ac">poe ke li ke poltxe san oe zasya'u</span>.
          </td>
          <td>
            (<em>
              <span class="vtr">Just occurred to</span> <span class="red bold">me</span> <span class="ag">this</span> <span class="ygold bold">which is</span> <span class="ac">she hasn't yet said, "I'm coming"</span>.
            </em> -or- <em>It just occured to me that she hasn't yet said she's coming.</em>)
          </td>
        </tr>
        <tr>
          <td>
            <span class="ag">Fì'ul</span> <span class="red bold">oeti</span> <span class="adj">nitram</span> <span class="vtr">'eykefu</span> nìtxan.
          </td>
          <td>
            (<em>
              <span class="ag">This</span> <span class="vtr">makes</span> <span class="red bold">me</span> <span class="vtr">feel</span> very <span class="adj">happy</span>.
            </em>)
          </td>
        </tr>
        <tr>
          <td>
            <span class="ag">Ful</span><span class="ygold bold">a</span> <span class="ac">nga nerume</span> <span class="red bold">oeti</span> <span class="adj">nitram</span> <span class="vtr">'eykefu</span> nìtxan
          </td>
          <td>
            (<em>
              <span class="ag">This</span> <span class="ygold bold">which is</span> <span class="ac">you are learning</span> <span class="vtr">makes</span> <span class="red bold">me</span> <span class="vtr">feel</span> very <span class="adj">happy</span>. -or- It makes me very happy that you're learning.
            </em>)
          </td>
        </tr>
      </table>
      <p>
        <span class="ag">Fì'ul</span> and <span class="ag">ful</span><span class="ygold bold">a</span> are used when the main sentence is made with a <span class="vtr">vtr.</span> verb. i.e. a verb that can do directly to something. such as hunt, eat, or touch. A lot of the time, <span class="ag">ful</span><span class="ygold bold">a</span> is seen with <span class="vtr">vingkap</span>, <span class="vtr">'eykefu</span>, and <span class="vtr">sleyku</span>. <span class="ag">Fì'ul</span> / <span class="ag">ful</span><span class="ygold bold">a</span> are the subject, the thing <em>doing</em> the <span class="vtr">action</span>.
      </p>
      <h2 id="futa">Futa</h2>
      <p>
        <span class="red bold">Fut</span><span class="ygold bold">a</span> is short for <span class="orange bold">fì'u</span>-<span class="red bold">t</span> <span class="ygold bold">a</span>, so this one could be used any time you could use <span class="red bold">fì'ut</span>.
      </p>
      <p>
        <u>For example:</u>
      </p>
      <table>
        <tr>
          <td>
            <span class="ag">Oel</span> <span class="vtr">fpìl</span> <span class="red bold">fì'ut</span>.
          </td>
          <td>
            (<em>
              <span class="ag">I</span> <span class="vtr">think</span> <span class="red bold">this</span>.
            </em>)
          </td>
        </tr>
        <tr>
          <td>
            <span class="ag">Oel</span> <span class="vtr">fpìl</span> <span class="red bold">fut</span><span class="ygold bold">a</span> <span class="ac">nga lu tstunwi</span>.
          </td>
          <td>
            (<em>
              <span class="ag">I</span> <span class="vtr">think</span> <span class="red bold">this</span> <span class="ygold bold">which is</span> <span class="ac">you are kind</span>.
            </em> -or- <em>I think that you are kind.</em>)
          </td>
        </tr>
        <tr>
          <td>
            <span class="vtr">Omum</span> <span class="ag">oel</span> <span class="red bold">fì'ut</span>.
          </td>
          <td>
            (<em>
              <span class="ag">I</span> <span class="vtr">know</span> <span class="red bold">this</span>.
            </em>)
          </td>
        </tr>
        <tr>
          <td>
            <span class="vtr">Omum</span> <span class="ag">oel</span> <span class="red bold">fut</span><span class="ygold bold">a</span> <span class="ac">tìfyawìntxuri oeyä perey aynga nìwotx</span>.
          </td>
          <td>
            (<em>
              <span class="ag">I</span> <span class="vtr">know</span> <span class="red bold">this</span> <span class="ygold bold">which is</span> <span class="ac">you all are waiting for my guidance</span>.
            </em> -or- <em>I know that you all are waiting for my guidance.</em>)
          </td>
        </tr>
        <tr>
          <td>
            <span class="vtr">Spaw</span> <span class="ag">oel</span> <span class="red bold">fì'ut</span>.
          </td>
          <td>
            <span class="ag">I</span> <span class="vtr">believe</span> <span class="red bold">this</span>.
          </td>
        </tr>
        <tr>
          <td>
            <span class="vtr">Spaw</span> <span class="ag">oel</span> <span class="red bold">fut</span><span class="ygold bold">a</span> <span class="ac">Mo’atìl tsole'a Neytirit</span>.
          </td>
          <td>
            (<em>
              <span class="ag">I</span>
              <span class="vtr">believe</span>
              <span class="red bold">this</span>
              <span class="ygold bold">which is</span>
              <span class="ac">Mo'at saw Neytiri</span>
            </em> -or- <em>I believe that Mo'at saw Neytiri.</em>)
          </td>
        </tr>
      </table>
      <p>
        <span class="red bold">Fì'ut</span> and <span class="red bold">fut</span><span class="ygold bold">a</span> are used when the main sentence is made with a <span class="vtr">vtr.</span> verb. i.e., a verb that can do directly to something. such as hunt, eat, touch. A lot of the time, <span class="red bold">fut</span><span class="ygold bold">a</span> is seen with <span class="vtr">fpìl</span>, <span class="vtr">spaw</span>, and <span class="vtr">omum</span>. <span class="red bold">Fì'ut</span> / <span class="red bold">ful</span><span class="ygold bold">a</span> are the thing <em>receiving</em> the <span class="vtr">action</span>.
      </p>
      <h2 id="furia">Furia</h2>
      <p>
        <span class="tp">Furi</span><span class="ygold bold">a</span> is short for <span class="orange bold">fì'u</span>-<span class="tp">ri</span> <span class="ygold bold">a</span>. So this one could be used any time <span class="tp">fì'uri</span> could be used.
      </p>
      <p>
        <u>For example:</u>
      </p>
      <table>
        <tr>
          <td>
            <span class="tp">Fì'uri</span> oeru <span class="orange bold">txoa</span> <span class="vin">livu</span>.
          </td>
          <td>
            (<em>
              <span class="tp">As for this</span>, <span class="vin">may there be</span> <span class="orange bold">forgiveness</span> to me.
            </em> -or- <em>I'm sorry for this.</em>)
          </td>
        </tr>
        <tr>
          <td>
            <span class="tp">Furi</span><span class="ygold bold">a</span> <span class="ac">txankrr fìtsengit ke tarmok</span>, oeru <span class="orange bold">txoa</span> <span class="vin">livu</span>.
          </td>
          <td>
            (<em>
              <span class="tp">as for this</span> <span class="ygold bold">which is</span> <span class="ac">for long time haven't been here</span>, <span class="vin">may there be</span> <span class="orange bold">forgiveness</span> to me.
            </em> -or- <em>I'm sorry for not being here for a long time.</em>)
          </td>
        </tr>
        <tr>
          <td>
            <span class="tp">Fì'uri</span> <span class="ag">ngal</span> <span class="vtr">tok</span> <span class="red bold">yìpet</span>?
          </td>
          <td>
            (<em>
              <span class="tp">As for this</span>, <span class="ag">you</span> <span class="vtr">are on</span> <span class="red bold">what level</span>?
            </em> -or- <em>How good are you at this?</em>)
          </td>
        </tr>
        <tr>
          <td>
            <span class="tp">Furi</span><span class="ygold bold">a</span> <span class="ac">täftxu</span> <span class="ag">ngal</span> <span class="vtr">tok</span> <span class="red bold">yìpet</span>?
          </td>
          <td>
            (<em>
              <span class="tp">As for this</span> <span class="ygold bold">which is</span> <span class="ac">weave</span>, <span class="ag">you</span> <span class="vtr">are on</span> <span class="red bold">what level</span>?
            </em> -or- <em>How's your weaving?</em>)
          </td>
        </tr>
        <tr>
          <td>
            <span class="tp">Fì'uri</span> <span class="orange bold">oe</span> <span class="vtr">'efu</span> <span class="adj">ye</span>.
          </td>
          <td>
            (<em>
              <span class="tp">As for this</span>, <span class="orange bold">I</span> <span class="vtr">feel</span> <span class="adj">satisfied</span>.
            </em>)
          </td>
        </tr>
        <tr>
          <td>
            <span class="tp">Furi</span><span class="ygold bold">a</span> <span class="ac">nga 'efu ye</span>, <span class="vtr">'efu</span> <span class="orange bold">oe</span> <span class="adj">ye</span> nìteng.
          </td>
          <td>
            (<em>
              <span class="tp">As for this</span> <span class="ygold bold">which is</span> <span class="ac">you feel satisfied</span>, <span class="orange bold">I</span> <span class="vtr">feel</span> <span class="adj">satisfied</span> too.
            </em>)
          </td>
        </tr>
      </table>
      <p>
        With <span class="tp">fì'uri</span> and <span class="tp">furi</span><span class="ygold bold">a</span>, it doesn't matter what kind of verb is used. A lot of the time, <span class="tp">furi</span><span class="ygold bold">a</span> is seen with <span class="vin">irayo si</span>, <span class="vin">tsap'alute si</span>, <span class="vin">pey</span>, <span class="vtr">'efu</span>, and <span class="vin">lu</span>. For more on <span class="tp">-ri</span>, check out <a href="09.php">Lesson Nine</a>. <img src="img/smiley.gif"/>
      </p>
      <p>
        Note that any of these word orders can be written backwards, and you will get sentences that look like some of those from Lesson Four. So that's pretty much it with these F words. Hopefully this has been helpful. Hayalovay! <img src="img/cheesy.gif"/>
