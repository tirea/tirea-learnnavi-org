      <div class="titlename">Plurals (me+, pxe+, ay+)</div>

      <p>In Na'vi, to express more than one of something, on the beginning of the
        noun or pronoun, put:
        <ul style="list-style: disc outside none !important">
          <li><b>me</b>+ to mean two of that thing</li>
          <li><b>pxe</b>+ for three</li>
          <li><b>ay</b>+ for or more, or unknown number</li>
        </ul>
      </p>
      <p>Tips:
        <ul style="list-style: disc outside none !important">
          <li>+ sign means change may occur to the first letter of the word you add this to.</li>
          <li>if <b>ay</b>+ is added to a word and a change occurs, <b>ay</b>+ is then optional. e.g. <b>ay</b> + <b>po</b> = <b>ayfo</b>, <b>ayfo</b> → <b>fo</b></li>
        </ul>
      </p>

      <br>

